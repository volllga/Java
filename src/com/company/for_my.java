package com.company;

public class for_my {
    public static void main(String[] args) {

        for (int i = 5; i >= 0; i--)
            System.out.print(i);

        System.out.println();

        int[] array = {2, 5, 7, 9};
        for (int n = 0; n <= 3; n++) {
            System.out.print(array[n]);

        }

    }
}
