package com.company;

public class switch_my {
    public static void main(String[] args) {
        int month_number = 4;

        switch (month_number){
            case 1:
                System.out.println("January");
                break;
            case 2:
                System.out.println("February");
            case 4:
                System.out.println("April");
            default:
                System.out.println("Can't find the month");

        }

    }


}

